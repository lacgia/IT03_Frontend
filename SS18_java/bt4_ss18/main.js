let number=parseInt(prompt("Nhập một số từ 0 đến 9:"));

switch(number){
    case 0: document.writeln("Số 0"); break;
    case 1: document.writeln("Số Một"); break;
    case 2: document.writeln("Số Hai"); break;
    case 3: document.writeln("Số Ba"); break;
    case 4: document.writeln("Số Bốn"); break;
    case 5: document.writeln("Số Năm"); break;
    case 6: document.writeln("Số Sáu"); break;
    case 7: document.writeln("Số Bảy");break;
    case 8: document.writeln("Số Tám"); break;
    case 9: document.writeln("Số Chín"); break;
default: document.writeln("nhap so sai");
}
